package Javadatatype;

public class IntegerLiterals {
    public static void main(String[] args) {
        int decimal = 267;      // Decimal (base 10)
        int octal = 0413;       // Octal (base 8)
        int hexadecimal = 0x10B; // Hexadecimal (base 16)
        int binary = 0b100001011; // Binary (base 2)

        System.out.println(decimal);      // 267
        System.out.println(octal);        // 267
        System.out.println(hexadecimal);  // 267
        System.out.println(binary);       // 267
    }
}


